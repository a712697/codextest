  INDEX idx_stock_out_items_order (order_id),
  INDEX idx_stock_out_items_product (product_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE stock_movements (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  product_id BIGINT NOT NULL,
  movement_type VARCHAR(32) NOT NULL,
  source_type VARCHAR(32) NOT NULL,
  source_id BIGINT NOT NULL,
  quantity_delta DECIMAL(12,3) NOT NULL,
  before_quantity DECIMAL(12,3) NOT NULL,
  after_quantity DECIMAL(12,3) NOT NULL,
  reason VARCHAR(255) NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  created_by BIGINT NULL,
  request_id VARCHAR(64) NULL,
  INDEX idx_stock_movements_product_time (product_id, created_at),
  INDEX idx_stock_movements_source (source_type, source_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE audit_logs (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  actor_id BIGINT NULL,
  actor_ip VARCHAR(64) NULL,
  module VARCHAR(64) NOT NULL,
  action VARCHAR(64) NOT NULL,
  target_type VARCHAR(64) NOT NULL,
  target_id BIGINT NULL,
  success BOOLEAN NOT NULL,
  message VARCHAR(255) NULL,
  before_data JSON NULL,
  after_data JSON NULL,
  request_id VARCHAR(64) NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  INDEX idx_audit_logs_actor_time (actor_id, created_at),
  INDEX idx_audit_logs_request_id (request_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

